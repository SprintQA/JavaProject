package com.class07;

public class IncrDecrOperator {
	public static void main(String args[]) {
		int i=5;
		
		
//		i = i+1; // Anothor way to do increment of value by 1
		
		++i; // Prefix operator        --->  i=6
		
		i++; // Postfix operator 	 	--->  i=7
		
		i--;						//	--->  i=6
		
		--i;						// ----> i=5
		
		System.out.println(i);
		
		
		
		
			
		
		
	}
}
