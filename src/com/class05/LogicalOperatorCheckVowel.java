package com.class05;

// Check given char is Vowel (a,e,i,o,u) or not

public class LogicalOperatorCheckVowel {
	public static void main(String args[]) {
		char ch = 'a';

		if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'u' || ch == 'o') {
			System.out.println("it's Vowel");
		} else {
			System.out.println("it's not vowel");
		}

	}
}
