package com.class03;

public class PriorityOfOperator {
	
	public static void main(String args[]) {
		
		System.out.println(20 / 4 * 5);  // 25
		
		System.out.println(20 * 4 / 20 * 10 / 2); // 20
		
		System.out.println(20 * 4 + 40 / 20 * 10 / 2 - 10);
		
//		80 + 2 * 10 / 2 - 10
//		80 + 10 - 10
		
		
	}
}
