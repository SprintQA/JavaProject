package com.class03;

public class IfElse {
	public static void main(String args[]) {
		System.out.println("1.Start the Car");
		System.out.println("2.Driving");

		boolean isTrafficOnRoute1 = false;

		if (isTrafficOnRoute1 == true) {
			System.out.println("3.Route 2");
		} else {
			System.out.println("4.Route 1");
		}

		System.out.println("5.Reached destination");
	}
}
