package com.class02;

public class ArithmeticOperator {
	public static void main(String args[]) {
		int num1 = 10, num2 = 20;

		// Addition
		System.out.println(num1 + num2);

		// Sub
		System.out.println(num1 - num2);

		// Mul
		System.out.println(num1 * num2);

		// Division
		System.out.println(num2 / num1);

	}
}
